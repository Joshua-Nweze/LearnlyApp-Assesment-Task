import mongoose, { Schema } from "mongoose";

let userSchema = new Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
}, {timestamps: true})

let Users = mongoose.model('Users', userSchema)

export default Users